-- EXEMPLO DE WHILE 
--ESTRUTURA DE REPETICAO

--APAGA A TABELA CASO EXISTA
DROP TABLE IF EXISTS TB_NUMEROS
GO

--CRIAR TABELA
CREATE TABLE TB_NUMEROS (
	NUMEROS INT
)
GO

--CONSTRUÇÃO DO CONTADOR
DECLARE @I INT
SET @I = 0

--CONDIÇÃO 
WHILE (@I <=20)
BEGIN
	INSERT INTO TB_NUMEROS VALUES (@I)
	SET @I = @I + 1
END
GO

--CONSULTA NA TABELA
SELECT * FROM TB_NUMEROS

