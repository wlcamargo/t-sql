--EXEMPLOS DE UNION

--UNION --> REMOVE OS DUPLICADOS NA JUNÇÃO
--UNION ALL --> JUNTA AS DUAS TABELAS INTEIRAS

DROP TABLE IF EXISTS TB_PRODUTOS_INATIVOS 
GO
CREATE TABLE TB_PRODUTOS_INATIVOS(
	ID_PRODUTO INT IDENTITY (1,1),
	PRODUTO VARCHAR(20),
	CATEGORIA VARCHAR(20),
	COR VARCHAR(10),
	PREÇO MONEY 
)
GO

INSERT INTO TB_PRODUTOS_INATIVOS VALUES
