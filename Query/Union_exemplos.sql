('PEN DRIVE','PERIFÉRICOS','VERMELHO',50),
('MP3 PLAYER','PERIFÉRICOS','AZUL',200),
('MP3 PLAYER','PERIFÉRICOS','BRANCO',200)
GO

SELECT * FROM TB_PRODUTOS_INATIVOS
SELECT * FROM TB_PRODUTO

--UNION
SELECT PRODUTO,PREÇO,'ATIVO' AS STATUS FROM TB_PRODUTO
UNION
SELECT  PRODUTO,PREÇO,'INATIVOS' AS STATUS FROM TB_PRODUTOS_INATIVOS

--UNION ALL
SELECT PRODUTO,PREÇO,'ATIVO' AS STATUS FROM TB_PRODUTO
UNION ALL
SELECT  PRODUTO,PREÇO,'INATIVOS' AS STATUS FROM TB_PRODUTOS_INATIVOS