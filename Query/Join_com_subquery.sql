GO

WITH AGG_COD AS (
SELECT 
	COD, 
	MAX(DATA_PREV) AS MAX_DATA_PREV
FROM 
	TB_DADOS
GROUP BY 
	COD
)
SELECT 
	AC.COD,
	AC.MAX_DATA_PREV,
	TF.DATA_REAL
FROM
	AGG_COD AC
	INNER JOIN 
		(SELECT COD, DATA_PREV, DATA_REAL FROM TB_DADOS) TF ON TF.COD = AC.COD 
															AND TF.DATA_PREV = AC.MAX_DATA_PREV
ORDER BY
	1
GO