CREATE FUNCTION FUNC_TB_PRODUTOS_VIP (@CUSTO MONEY)
RETURNS TABLE
AS RETURN
SELECT
	PRODUTO,
	CUSTO_UNIT
FROM
	TB_ESTOQUE
WHERE
	CUSTO_UNIT >= @CUSTO
GO

