--EXEMPLO FUNCTION DE TABELA

--CRIAR TABELA
CREATE TABLE TB_ESTOQUE (
	ID INT IDENTITY(1,1),
	PRODUTO VARCHAR(20),
	CUSTO_UNIT MONEY,
	QTD INT
)
GO

